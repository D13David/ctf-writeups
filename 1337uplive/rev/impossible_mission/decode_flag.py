data = [0x94, 0xE5, 0x47, 0x97, 0x70, 0x20, 0x92, 0x42, 0x9C, 0xBE, 0x69, 0x58, 0x0F, 0x2E, 0xFB, 0x6A, 0xC4, 0x26, 0xE7, 0x36, 0x17, 0x23, 0xA0, 0x20, 0x2F, 0x0B, 0xCD]
flag = ""

for i, c in enumerate(data):
    # xor with key
    c ^= i
    # swap nibbles
    c = ((c & 0x0F) << 4 | (c & 0xF0) >>4)
    flag = flag + chr(c)

print(flag)